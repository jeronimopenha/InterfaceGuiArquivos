    private void jbtnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtnExcluirMouseClicked
        if (jtblProduto.getSelectedRow() == -1) {
            return;
        }
        this.modelo.removeRow(jtblProduto.getSelectedRow());

        String update = "";
        for (int i = 0; i < modelo.getRowCount(); i++) {
            for (int j = 0; j < persistirProduto.NUM_COLUMNS; j++) {
                update += (String) modelo.getValueAt(i, j);
                if ((j == (persistirProduto.NUM_COLUMNS - 1))
                        && (i < (modelo.getRowCount() - 1))) {
                    update += "\n";
                } else {
                    update += ";";
                }
            }
        }
        persistirProduto.atualizarProdutos(update);
    }//GEN-LAST:event_jbtnExcluirMouseClicked

    private void jbtnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtnEditarMouseClicked
        if (jtblProduto.getSelectedRow() == -1) {
            return;
        }

        janelaEdicaoProduto janelaEdicaoProduto = new janelaEdicaoProduto(null, true, this);

        janelaEdicaoProduto.getJtxtDescricao().setText((String) modelo.getValueAt(jtblProduto.getSelectedRow(), 0));
        
        janelaEdicaoProduto.setVisible(true);

    }//GEN-LAST:event_jbtnEditarMouseClicked

    public void preencherTabela(Object[][] o) {
        if (o == null) {
            return;
        }

        for (int i = 0; i < o.length; i++) {
            this.modelo.addRow(o[i]);
        }
    }

    
