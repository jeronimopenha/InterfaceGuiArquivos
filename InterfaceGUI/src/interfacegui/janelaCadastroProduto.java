    private void jbtnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtnSalvarMouseClicked
        if (jtxtDescricao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar!\n"
                    + "Existem campos n√£o preenchidos.");
            return;
        }
        String novoProduto;

        novoProduto = jtxtDescricao.getText();
        persistirProduto.salvarNovoProduto(novoProduto);
        limparCampos();
    }//GEN-LAST:event_jbtnSalvarMouseClicked

    public void limparCampos() {
        jtxtDescricao.setText("");
    }

